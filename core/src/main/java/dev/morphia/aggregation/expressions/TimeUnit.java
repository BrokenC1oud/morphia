package dev.morphia.aggregation.expressions;

/**
 * Defines units of time for use in expressions
 */
public enum TimeUnit {
    /**
     * year
     */
    YEAR,
    /**
     * quarter
     */
    QUARTER,
    /**
     * month
     */
    MONTH,
    /**
     * week
     */
    WEEK,
    /**
     * day
     */
    DAY,
    /**
     * hour
     */
    HOUR,
    /**
     * minute
     */
    MINUTE,
    /**
     * second
     */
    SECOND,
    /**
     * millisecond
     */
    MILLISECOND
}
